    private void SetText(Reader reader) {
        txtReaderID.setText(reader.getReaderID());
        txtReaderID.setEditable(false);
        txtFullName.setText(reader.getReadername());
        boolean gender = reader.isGender();
        if (gender == true) {
            rdMale.setSelected(true);
        } else if (gender == false) {
            rdFemale.setSelected(true);
        }
        chooseDate.setDate(reader.getBirthday());
        txtAddress.setText(reader.getAddress());
        txtEmail.setText(reader.getEmail());
        txtPhone.setText(reader.getPhone());
    }

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        boolean result = false;
        String readerID = txtReaderID.getText().trim().toUpperCase();
        if (!readerID.isEmpty()) {
            if (!readerID.matches("^\\w{1,10}$")) {
                JOptionPane.showMessageDialog(this, "Wrong input please refill ReaderID.");
            } else {
                for (int i = 0; i < list.size(); i++) {
                    if (readerID.equals(list.get(i).getReaderID())) {
                        SetText(list.get(i));
                        jTable1.addRowSelectionInterval(i, i);
                        result = true;
                    }
                }
                if (result == false) {
                    JOptionPane.showMessageDialog(this, "This reader ID is not exist");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please fill ReaderID.");
        }
    }//GEN-LAST:event_btnSearchActionPerformed

