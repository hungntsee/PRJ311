    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String readerID = txtReaderID.getText().trim().toUpperCase();
        if (!readerID.isEmpty()) {
            for (int i = 0; i < list.size(); i++) {
                if (list.get(i).getReaderID().equals(readerID)) {
                    JOptionPane.showMessageDialog(this, "Reader ID can not be duplicated.");
                    return;
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Reader ID can not be empty.");
            return;
        }
        String fullname = txtFullName.getText().trim();
        if (fullname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Full name is empty.");
            txtFullName.requestFocus();
            return;
        }
        boolean gender = true;
        if (rdMale.isSelected()) {
            gender = true;
        } else if (rdFemale.isSelected()) {
            gender = false;
        } else {
            JOptionPane.showMessageDialog(this, "Please choose your gender");
            return;
        }
        Date birthday = chooseDate.getDate();
        if (birthday == null) {
            JOptionPane.showMessageDialog(this, "Please choose your birthday");
            return;
        }
        String email = txtEmail.getText().trim();
        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Email is required!!!");
            return;
        }
        String phone = txtPhone.getText().trim();
        if (phone.isEmpty()) {
            JOptionPane.showMessageDialog(this, " Phone is required!!!");
            return;
        }
        String address = txtAddress.getText().trim();
        if (address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Address is required!!!");
            return;
        }

        ValidData valid = new ValidData(readerID, fullname, gender, birthday, address, email, phone);

        if (valid.checkValid()) {

            Reader reader = new Reader(readerID, fullname, gender, birthday, address, email, phone);
            boolean result = false;

            if (addNew == true) {
                result = list.add(reader);
                addNew = false;
            } else if (addNew == false) {
                for (int i = 0; i < list.size(); i++) {
                    if (reader.getReaderID().equals(list.get(i).getReaderID())) {
                        list.remove(list.get(i));
                        list.add(reader);
                        result = true;
                    }
                }
            }
            if (result = true) {
                JOptionPane.showMessageDialog(this, "AddNew/Update successfully!");
                jTable1.updateUI();
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

