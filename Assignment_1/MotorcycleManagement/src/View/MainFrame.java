    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        // TODO add your handling code here:
        String motorcycleID = txtMotorcycleID.getText().toUpperCase();
        String model = txtModel.getText();
        Date year = null;
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        if (!txtYear.getText().matches("\\d{4}\\-\\d{2}\\-\\d{2}$")) {
            JOptionPane.showMessageDialog(this, "Wrong input. (yyyy-MM-dd)");
            txtYear.requestFocus();
            return;
        } else {
            year = Date.valueOf(txtYear.getText());
        }
        String condition = txtCondition.getText();
        double price;
        if (isNumber(txtPrice)) {
            JOptionPane.showMessageDialog(this, "Input must be a number");
            txtPrice.requestFocus();
            return;
        } else {
            price = Double.parseDouble(txtPrice.getText());
        }
        int quantity;
        if (isNumber(txtQuantity)) {
            JOptionPane.showMessageDialog(this, "Input must be a number");
            txtQuantity.requestFocus();
            return;
        } else {
            quantity = Integer.parseInt(txtQuantity.getText());
        }
        String warranty = txtWarranty.getText();
        brandModel brand = (brandModel) cbBrandName.getSelectedItem();
        String brandID = brand.getBrandID();
        motorcycleModel cycle;
        cycle = new motorcycleModel(motorcycleID, model, year, condition, price, quantity, warranty, brand);
        motorcycleDAO cycleController = new motorcycleDAO();
        if (isEmpty(txtMotorcycleID) || isEmpty(txtModel) || isEmpty(txtYear) || isEmpty(txtCondition) || isEmpty(txtPrice) || isEmpty(txtQuantity) || isEmpty(txtWarranty)) {
            JOptionPane.showMessageDialog(this, "Please fill sandatory fields");
        } else if (cycles.isDuplicated(motorcycleID)) {
            JOptionPane.showMessageDialog(this, "The motorcycleID is existed!!");
        } else {
            int n = 0, m = 0;
            if (addNew == true) {
                n = cycleController.AddNew(cycle);
            } else {
                m = cycleController.Update(cycle);
            }
            String msg = "An motorcycle has been added/updated.";
            try {
                if (n > 0 || m > 0) {
                    JOptionPane.showMessageDialog(this, msg);
                    if (addNew == false) {
                        int pos = tblMotorcycle.getSelectedRow();
                        cycleModel.getCycles().set(pos, cycle);
                    } else {
                        cycleModel.getCycles().add(cycle);
                    }
                    tblMotorcycle.updateUI();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
            addNew = false;
        }

    }//GEN-LAST:event_btnSaveActionPerformed

