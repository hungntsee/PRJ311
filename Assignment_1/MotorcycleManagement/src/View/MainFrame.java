    private void btnDeleteBrandActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteBrandActionPerformed
        // TODO add your handling code here:
        int pos = tblBrand.getSelectedRow();
        brandDAO brand = new brandDAO();
        String brandID = txtBrandID.getText().toUpperCase();

        if (findByBrand(brandID) > -1) {
            JOptionPane.showMessageDialog(this, "Can not delete this Brand " + brands.findBrand(brandID).toString());
        } else {
            String msg = " The brand " + brandID + " has been deleted from DB!";
            if (brandID == null) {
                JOptionPane.showMessageDialog(this, "Nothing to delete!!");
            } else {
                try {
                    int n = brand.deleteBrand(brandID);
                    if (n > 0) {
                        JOptionPane.showMessageDialog(this, msg);
                        brandFullModel.getBrands().removeElementAt(pos);
                        tblBrand.updateUI();
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e);
                }
            }
        }
    }//GEN-LAST:event_btnDeleteBrandActionPerformed

