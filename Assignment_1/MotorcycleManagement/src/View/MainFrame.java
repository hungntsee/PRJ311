    public boolean isEmpty(JTextField txt) {
        if (txt.getText().equals("")) {
            txt.requestFocus(true);
            return true;
        }
        return false;
    }

    public boolean isNumber(JTextField txt) {
        if (!txt.getText().matches("[\\d]+")) {
            return true;
        }
        return false;
    }
    public int findByBrand(String brandID){
        brandModel brand = brands.findBrand(brandID);
        for (int i = 0; i < cycles.size(); i++) {
            if(brand.equals(cycles.get(i).getBrand())) return i;
        }
        return -1;
    }
